package calculator;

public interface ICalculator {

	// method that returns sum of two numbers
	int add(int x, int y);

	// method that returns subtraction of two numbers
	int sub(int x, int y);

	// method that returns multiply of two numbers
	int mul(int x, int y);

	// method that returns the division of two numbers
	int div(int x, int y);
}
